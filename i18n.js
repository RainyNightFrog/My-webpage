  function getLang() {
    var saved = localStorage.getItem(STORAGE_KEY);
    if (saved === "zh") {
      saved = "zhHant";
      localStorage.setItem(STORAGE_KEY, saved);
    }
    if (saved && messages[saved]) return saved;
    return DEFAULT_LANG;
  }

  function setLang(lang) {
    if (!messages[lang]) lang = DEFAULT_LANG;
    localStorage.setItem(STORAGE_KEY, lang);
    try {
      document.dispatchEvent(
        new CustomEvent("lc:langchange", { detail: { lang: lang } })
      );
    } catch (e) {}
  }

  function getNested(obj, path) {
    var parts = path.split(".");
    var cur = obj;
    for (var i = 0; i < parts.length; i++) {
      if (!cur || cur[parts[i]] === undefined) return undefined;
      cur = cur[parts[i]];
    }
    return cur;
  }

  function format(str, vars) {
    if (!vars) return str;
    return str.replace(/\{(\w+)\}/g, function (_, key) {
      return vars[key] !== undefined ? String(vars[key]) : "{" + key + "}";
    });
  }

  function t(path, vars) {
    var lang = getLang();
    var text = getNested(messages[lang], path);
    if (text === undefined) text = getNested(messages[DEFAULT_LANG], path);
    if (text === undefined) return path;
    return format(text, vars);
  }

